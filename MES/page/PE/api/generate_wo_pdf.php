            <span class="label">START TIME</span>
            <div style="font-size: 1px; line-height: 1px;">&nbsp;</div>
            <span class="value">' . formatDateTH_PDF($wo['started_at']) . ' ' . formatTime_PDF($wo['started_at']) . '</span>
        </td>
        <td width="25%">
