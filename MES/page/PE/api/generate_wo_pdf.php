                <div style="font-size: 8px; color: #7f8c8d; margin-bottom: 2px;">AFTER</div>';
if ($photoAfterPath) {
    $html .= '<img src="' . $photoAfterPath . '" height="120" border="0">';
} else {
    $html .= '<br><br><br><span style="color:#ccc; font-size:10px;">- No Image -</span>';
}
$html .= '  </div>
        </td>
    </tr>
</table>

