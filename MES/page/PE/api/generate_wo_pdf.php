            <span class="label">SPARE PARTS (อะไหล่ที่ใช้)</span><br>
            <div style="border-bottom: 1px dotted #ccc; padding: 2px 0;">';
            
$parts = preg_split('/[\r\n]+/', $wo['parts_used'] ?? '');
$hasParts = false;
$partsHtml = '';
foreach ($parts as $part) {
    $part = trim($part);
    if (!empty($part)) {
